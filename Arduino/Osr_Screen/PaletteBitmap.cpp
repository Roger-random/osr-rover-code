// Given an index into bitmap array and an instance of RGBmatrixPanel
// Draw the bitmap into panel via palette color lookup
void PaletteBitmap::draw_bitmap(int index, RGBmatrixPanel* panel) {
  int pixel=0;
  for (int y = 0; y < 16;y++) {
    for (int x = 0; x < 32;x++) {
      panel->drawPixel(x,y,palette[bitmaps[index][pixel++]]);
    }
  }
  return;
}
